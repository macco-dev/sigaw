/* Get glyph data for a character. Returns nullptr for out-of-range chars. */
static inline const uint8_t* font_glyph(char c) {
    int idx = (int)(unsigned char)c - FONT_FIRST;
    if (idx < 0 || idx >= FONT_COUNT) return nullptr;
    return font_data[idx];
}

/* Measure text width in pixels at a given scale. */
static inline int font_text_width(const char* text, int scale = 1) {
    int w = 0;
    while (*text) {
        w += FONT_W * scale;
        text++;
    }
    return w;
}

